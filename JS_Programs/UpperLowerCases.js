//Conversion of Uppercase letters to lowercase letters and vice-versa

const arr1 = ['a', 'b', 'c', 'd', 'e'];
const arr2 = ['A', 'B', 'C', 'D', 'E'];

console.log("Conversion of lowercase letters to uppercase letters");
for (let i = 0; i < arr1.length; i++){
    console.log(arr1[i].toUpperCase());
}

console.log("Conversion of uppercase letters to lowercase letters");
for (let i = 0; i < arr1.length; i++){
    console.log(arr2[i].toLowerCase());
}