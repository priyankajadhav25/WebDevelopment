//Array Operations

const arr = ["Priyanka", 19, 20, 21];
console.log("Initial array...");

for (let i = 0; i < arr.length; i++){
    console.log(arr[i]);
}

console.log("Array after Push Operation");
arr.push(60);
console.log(arr);

console.log("Array after Pop Operation");
arr.pop();
console.log(arr);

console.log("Array after Unshift Operation");
arr.unshift("Jadhav");
console.log(arr);

console.log("Array after Shift Operation");
arr.shift();
console.log(arr);

console.log("Array after sort Operation");
arr.sort();
console.log(arr);





